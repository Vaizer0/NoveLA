package my.noveldokusha.core.domain

/**
 * Восстановление согласованности пагинированного списка глав (parsePage).
 *
 * Инкрементальное обновление доверяет сохранённому счётчику страниц
 * [my.noveldokusha.feature.local_database.tables.Book.chaptersLastPage] и перечитывает
 * только последнюю страницу + новые. Если главы в БД потерялись/рассинхронизировались,
 * средние страницы (2..N-1) никогда не перечитываются и остаются дыры.
 *
 * Этот предикат — дешёвая проверка перед тем как доверять счётчику. Если она провалилась,
 * счётчик сбрасывается в null и запускается полный репарс (позиции пересобираются 0..n-1).
 */
object ChapterPagination {

    /**
     * Консервативный пол глав на страницу. Не реальный размер страницы источника
     * (у реальных источников 100–200), а порог, ниже которого данные точно битые.
     * Именно потому что пол сильно ниже реального размера, он не даёт ложных срабатываний
     * на неполной последней странице и не протухает при смене размера страницы сайтом.
     */
    const val MIN_CHAPTERS_PER_PAGE = 30

    /**
     * @return true если число глав в БД согласуется со счётчиком страниц.
     *
     * Гарантированно заполнены только страницы 1..N-1, последняя может быть неполной,
     * поэтому нижняя граница = (N-1) * MIN_CHAPTERS_PER_PAGE + 1.
     * При lastPage == 1 порог равен 1 — проверка вырождается в "есть хотя бы одна глава"
     * (для одной страницы инварианта пагинации нет, репарс не нужен).
     */
    fun isPageCounterConsistent(lastPage: Int, chapterCount: Int): Boolean =
        lastPage > 0 && chapterCount >= (lastPage - 1) * MIN_CHAPTERS_PER_PAGE + 1
}
